public class GameEntity {
    private int halth;
    private int damage;


    public int getHalth() {
        return halth;
    }

    public void setHalth(int halth) {
        this.halth = halth;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

}
